    public void eliminar(int pos) {
        Iterator<Nodo> itrnodo = lista.iterator();
        while (itrnodo.hasNext()) {
            Nodo listado = itrnodo.next();
            if (listado.getPosi() == pos) {
                System.out.println("encontrado " + listado.getPosi());
                lista.remove(pos);
            }
        }
    }

